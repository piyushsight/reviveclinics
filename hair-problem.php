                        <p>Androgenetic alopecia (AGA) is by far the most common cause of hair loss. It affects approximately 50% of men by the age of 50 and 20% to 53% of women by the age of 50. Although it is a medically benign condition, it can have a significant psycho-social impact for patients. AGA involves both genetic and hormonal factors. Genetics determine both the density and the location of androgen-sensitive hair follicles on site-specific areas of the scalp.</p>
                        <h4 class="h-light color marginbot-10">Etiology- Hormonal factors</h4>
                        <p>A major determinant of AGA is intracellular androgen metabolism, involves two steroid- metabolizing enzymes (5a-reductase I &II) and androgen receptor proteins. Variances in levels of these androgen-metabolizing enzymes and androgen receptor proteins help explain the differences between balding and non-balding scalp at various ages and the different clinical patterns and severities between men and women.</p>
                        <p>5α-reductase isoenzymes type I and II, are both part of normal androgen metabolism and reduce testosterone (T) to dihydrotestosterone (DHT) ndrogens act on tissues via binding to a specific intracellular protein, the androgen receptor, a member of the steroid-thyroid nuclear receptor super family.</p>
                        <p>In the presence of ligand, androgen receptors undergo a change in conformation, resulting in an activated form that binds as a homodimer with a specific androgen response element on the target gene. The hormone–receptor complex then activates transcription of target gene.</p>
                        <p>A large number of proteins (co-regulators) regulate the activity of the hormone–receptor complex, both enhancing (co-activators) and suppressing (co-repressors) transcriptional activation.</p>
                        <p>Androgen receptor proteins (ARP) are found in the outer root sheath and dermal papilla fibroblasts of scalp hair follicles. The receptor levels were found to be 30% greater in balding frontal hair follicles than in non-balding occipital follicles in both men and women with AGA, but the total receptor content is 40% less in women than in men. The binding of androgens to ARP results in modification of signal transduction between the mesenchymal-derived dermal papilla and the epithelial-derived follicular cells. These events within the follicle result in the transformation of terminal to miniaturized hair follicles on the scalp in AGA. Paradoxically, in other androgen sensitive areas such as the beard and mustache, androgens increase, rather than decrease, hair follicles at puberty. The explanation for this bifurcated action is not known.</p>
                        <p>A role for androgens in the aetiology of balding is incontrovertible. Nevertheless, other factors are clearly involved as not all men develop balding despite having similar androgen levels to those that do.</p>
                        <h4 class="h-light color marginbot-10">Genetic factors</h4>
                        <p>The exact inheritance pattern of AGA is believed to be most likely autosomal dominant, polygenic, and inherited from either parent.</p>
                        <h4 class="h-light color marginbot-10">Pathogenesis with histological changes-</h4>
                        <p>The DHT hormone which is produced as byproduct of testosterone at puberty affects genetically programmed hair follicles, predominantly fronto- vertical scalp in two ways-</p>
                        <p>1. The hair life cycle is altered, progressive shortening of the anagen phase occurring over many cycles It decreases the duration of Anagen ( growth phase of hair) so hairs goes in talegen phase ( fall phase of hair) more frequently so hair fall occurs.</p>
                        <p>2. The DHT Hormone gets attached with hair follicle which contains certain DHT receptor proteins which transform healthy follicles to miniaturized follicles.</p>
                        <p>Subsequent miniaturization of hairs leads to decreased scalp coverage. These finer small vellus-like hairs of varying lengths and diameters are the hallmark of AGA.</p>
